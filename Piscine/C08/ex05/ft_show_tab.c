#include <unistd.h>
#include "ft_stock_str.h"

void	print_size(int size)
{
	char	c;

	if (!size)
		return ;
	print_size(size / 10);
	c = size % 10 + '0';
	write (1, &c, 1);
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str)
	{
		write(1, par[i].str, par[i].size);
		write(1, "\n", 1);
		if (par[i].size == 0)
			write(1, "0", 1);
		else
			print_size(par[i].size);
		write(1, "\n", 1);
		write(1, par[i].copy, par[i].size);
		write(1, "\n", 1);
		i++;
	}
}
